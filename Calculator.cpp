#include <iostream>
#include <string>

using namespace std;

int main()
{
    double firstvalue;
    double secondvalue;
    string operand;
    double total;
    cout << "Please enter your first value:" << endl;
    cin >> firstvalue;
    cout << "Please enter an operand following " << firstvalue << endl;
    cin >> operand;
    cout << "Please enter a second value:" << endl;
    cin >> secondvalue;
    if(operand == "+")
    {
        total = (firstvalue + secondvalue);
    }
    if(operand == "-")
    {
        total = (firstvalue - secondvalue);
    }
    if(operand == "/")
    {
        total = (firstvalue / secondvalue);
    }
    if(operand == "*")
    {
        total = (firstvalue * secondvalue);
    }
    cout << "The answer is " << total << endl;
    return 0;
}

